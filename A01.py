# 10진수
decimal_number = 118

# 10진수를 2진수로 변환
binary_number = bin(decimal_number)
print(f"{decimal_number} in binary: {binary_number}")

# 10진수를 8진수로 변환
octal_number = oct(decimal_number)
print(f"{decimal_number} in octal: {octal_number}")

# 10진수를 16진수로 변환
hexadecimal_number = hex(decimal_number)
print(f"{decimal_number} in hexadecimal: {hexadecimal_number}")
